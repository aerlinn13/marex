import * as React from "react";
import { cva, type VariantProps } from "class-variance-authority";
import { cn } from "@/lib/utils";

const badgeVariants = cva(
  "inline-flex items-center rounded-full border px-2.5 py-0.5 text-xs font-semibold transition-colors focus:outline-none focus:ring-2 focus:ring-ring focus:ring-offset-2",
  {
    variants: {
      variant: {
        default: "border-transparent bg-primary text-primary-foreground hover:bg-primary/80",
        secondary: "border-transparent bg-secondary text-secondary-foreground hover:bg-secondary/80",
        destructive: "border-transparent bg-destructive text-destructive-foreground hover:bg-destructive/80",
        outline: "text-foreground",
        success: "border-transparent bg-green-500/10 text-green-400",
        warning: "border-transparent bg-yellow-500/10 text-yellow-400",
        processing: "border-transparent bg-blue-500/10 text-blue-400",
        active: "border-transparent bg-marex-accent-purple/20 text-marex-accent-purple",
        filled: "border-transparent bg-marex-positive/10 text-marex-positive",
        cancelled: "border-transparent bg-muted text-muted-foreground",
        suspended: "border-transparent bg-marex-warning/10 text-marex-warning",
        sell: "border-transparent bg-marex-sell/10 text-marex-sell",
        buy: "border-transparent bg-marex-buy/10 text-marex-buy",
      },
    },
    defaultVariants: {
      variant: "default",
    },
  }
);

export interface BadgeProps
  extends React.HTMLAttributes<HTMLDivElement>,
    VariantProps<typeof badgeVariants> {}

function Badge({ className, variant, ...props }: BadgeProps) {
  return <div className={cn(badgeVariants({ variant }), className)} {...props} />;
}

export { Badge, badgeVariants };
